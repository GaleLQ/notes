// Set
// 大多数可以保证插入的顺序
// new Set([iterable]);
// Set.prototype.size(感觉就是实例上的属性) 返回元素的个数
// Set.prototype.add() 末尾添加一个值,返回自身
// Set.prototype.clear() 清空所有,无返回
// Set.prototype.delete() 删除指定,返回是否成功布尔
// Set.prototype.entries() 返回一个包含[key, value]的可迭代对象
// Set.prototype.forEach() 循环
// Set.prototype.has() 返回这个值是否存在的布尔
// Set.prototype.values() 返回一个包含每个值的可迭代对象,.keys是其别名
// Set.prototype[@@iterator]() 自身可迭代